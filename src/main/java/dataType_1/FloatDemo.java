package dataType_1;

public class FloatDemo 
{

	public static void main(String[] args)
	{
		
		float  my_marks = (float) 60.89;
		
		float my_maths_marks =89.45f;
		
		double my_new_marks = 4545.29;
		
		System.out.println(my_marks);
		
		System.out.println(my_maths_marks);
		
		System.out.println(my_new_marks);

		int a=(int)45.49 ;
		//Type Casting as up casting
		
		float b=(float)45;
		//Type Casting as down casting
		
		System.out.println(a);
		System.out.println(b);
			
	}

}
