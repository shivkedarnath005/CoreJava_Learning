package iterativeStatements_7;

public class DoWhile_PrintNumbers 
{
    public static void main(String[] args) 
    {
        int i = 1;//loop variable initialization
        do 
        {
            System.out.println(i);//code logic here
            i++;  //increment or decrement loop variable
        } 
        while (i <= 10); //loop condition execute at least once
    }
    
}

