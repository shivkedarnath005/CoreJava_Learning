package iterativeStatements_7;

public class PrintArray 
{
    public static void main(String[] args) 
    {
        String[] fruits = {"Apple", "Banana", "Cherry", "Date", "Elderberry", "Orange"};
        for (int i = 0; i < fruits.length; i++) 
        {
            System.out.println(fruits[i]);
        }
    }
}
